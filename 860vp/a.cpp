void solve() {
    int n;
    cin >> n;
    vi a(n), b(n);
    for (int i = 0; i < n; i++) {
    	cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
    	cin >> b[i];
    }
    for (int i = 0; i < n; i++) {
    	if (a[i] > b[i]) swap(a[i], b[i]);
    }
    if (a[n-1] == *max_element(a.begin(), a.end()) && b[n-1] == *max_element(b.begin(), b.end())) {
    	cout << "YES" << endl;
    } else {
    	cout << "no" << endl;
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}

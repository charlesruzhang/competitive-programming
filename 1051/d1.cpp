const ll MOD = 1e9+7;
const ll MAXX = 1e16;
const int INF = 1e9 + 7;
template<typename T>
struct Fenw {
    int n;
    vector<T> fenw;
    
    Fenw(int n) : n(n), fenw(n, 0) {}

    void update(int i, T delta) {
        for (; i < n; i = i | (i + 1))
            fenw[i] += delta;
    }

    T query(int i) {
        T sum = 0;
        for (; i >= 0; i = (i & (i + 1)) - 1)
            sum += fenw[i];
        return sum;
    }

    T range_query(int l, int r) {
        return query(r) - (l > 0 ? query(l - 1) : 0);
    }
};

struct mi {
	int v;
	explicit operator int() const { return v; }
	mi() { v = 0; }
	mi(long long _v) : v(_v % MOD) { v += (v < 0) * MOD; }
};
mi &operator+=(mi &a, mi b) {
	if ((a.v += b.v) >= MOD) a.v -= MOD;
	return a;
}
mi &operator-=(mi &a, mi b) {
	if ((a.v -= b.v) < 0) a.v += MOD;
	return a;
}
mi operator+(mi a, mi b) { return a += b; }
mi operator-(mi a, mi b) { return a -= b; }
mi operator*(mi a, mi b) { return mi((long long)a.v * b.v); }
mi &operator*=(mi &a, mi b) { return a = a * b; }
mi pow(mi a, long long p) {
	assert(p >= 0);
	return p == 0 ? 1 : pow(a * a, p / 2) * (p & 1 ? a : 1);
}
mi inv(mi a) {
	assert(a.v != 0);
	return pow(a, MOD - 2);
}
mi operator/(mi a, mi b) { return a * inv(b); }
void solve() {
    int n;
    cin >> n;
    vi a(n);
    for (int i = 0; i < n; i++) {
    	cin >> a[i];
    }
    Fenw<int> F(n+1);
    vector<mi> dp(n);
    vector<mi> p2(n+1, 1);
    for (int i = 1; i <= n; i++) {
    	p2[i] = p2[i-1] * 2;
    }
    mi tot = p2[n];
    mi pref = 0;
    for (int i = 0; i < n; i++) {
    	int gr = F.range_query(a[i] + 1, n);
    	mi cnt = (p2[gr] - 1) * p2[(i - gr)];
    	dp[i] = cnt;
    	int c = 0;
    	mi ans = 0;
    	for (int j = i - 1; j >= 0; j--) {
    		if (a[j] > a[i]) {
    			ans += dp[j] * p2[c];
    		} else if (a[j] <= a[i]) {
    			c++;
    		}
    	}
    	pref += ans;
    	tot -= pref;
    	F.update(a[i], 1);
    }
    cout << (int) tot << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
